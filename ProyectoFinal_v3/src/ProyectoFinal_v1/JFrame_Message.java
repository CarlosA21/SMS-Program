    private void btn_enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enviarActionPerformed
        try {
            if(txtapi.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Escriba la clave API","Error", JOptionPane.ERROR_MESSAGE);
            }
            else if(txtnumber.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Ingrese los numeros telefonicos","Error", JOptionPane.ERROR_MESSAGE);
            }
            else if(txtmessage.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Escriba un mensaje","Error", JOptionPane.ERROR_MESSAGE);
            }
            else{
                // Construct data
                String apiKey = "apikey=" + txtapi.getText();
                String message = "&message=" + txtmessage.getText();
                String sender = "&sender=" + txtsender.getText();
                String numbers = "&numbers=" + txtnumber.getText();

                // Send data
                HttpURLConnection conn = (HttpURLConnection) new URL("https://api.txtlocal.com/send/?").openConnection();
                String data = apiKey + numbers + message + sender;
                conn.setDoOutput(true);
                conn.setRequestMethod("POST");
                conn.setRequestProperty("Content-Length", Integer.toString(data.length()));
                conn.getOutputStream().write(data.getBytes("UTF-8"));
                final BufferedReader rd = new BufferedReader(new InputStreamReader(conn.getInputStream()));
                final StringBuffer stringBuffer = new StringBuffer();
                String line;
                while ((line = rd.readLine()) != null) {
                    //stringBuffer.append(line);
                    JOptionPane.showMessageDialog(null, "Mensaje Enviado correctamente");
                }
                rd.close();
            }
            //return stringBuffer.toString();
        } catch (Exception e) {
            //System.out.println("Error SMS "+e);
            JOptionPane.showMessageDialog(null, e);
            //return "Error "+e;
        }
    }//GEN-LAST:event_btn_enviarActionPerformed

